    public void generateFile(String in, String out) throws FileNotFoundException {
        FileInputStream fis = new FileInputStream(in);
        FileOutputStream fos = new FileOutputStream(out);
        Scanner sc = new Scanner(fis);
        PrintWriter pr = new PrintWriter(fos);

        while (sc.hasNextLine()){
            String s = sc.nextLine();
            if (s.startsWith("#")){
                continue;
            }
            s = s.replaceFirst("\t"," ").replace("\t","");
            String[] split = s.split(" ");
            pr.write("public static String "+split[1].toUpperCase()+" = \""+split[0]+"\";\n");
            //System.out.println(split[0]+" --- "+split[1]);
        }
        pr.flush();
        pr.close();
    }

    public void generateFromName(String in, String out) throws FileNotFoundException {
        FileInputStream fis = new FileInputStream(in);
        FileOutputStream fos = new FileOutputStream(out);
        Scanner sc = new Scanner(fis);
        PrintWriter pr = new PrintWriter(fos);

        pr.write("switch(f) {");
        while (sc.hasNextLine()){
            String s = sc.nextLine();
            if (s.startsWith("#")){
                continue;
            }
            s = s.replaceFirst("\t"," ").replace("\t","");
            String[] split = s.split(" ");
            String n;
            String t;
            n = split[1];
            t = split[0].split("/")[0];
            t = Character.toUpperCase(t.charAt(0))+t.substring(1).toLowerCase();
            pr.write("\tcase \""+n.toUpperCase()+"\": return "+t+"."+n.toUpperCase()+";\n");
            //System.out.println(split[0]+" --- "+split[1]);
        }
        pr.write("}\n");
        pr.flush();
        pr.close();
    }

